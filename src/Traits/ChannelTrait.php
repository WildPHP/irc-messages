declare(strict_types=1);

namespace WildPHP\Messages\Traits;

trait ChannelTrait
{
    protected $channel = '';

    /**
     * @return string
     */
    public function getChannel(): string
    {
        return $this->channel;
    }

    /**
     * @param string $channel
     */
    public function setChannel(string $channel): void
    {
        $this->channel = $channel;
    }
}
